# FUNCIONES
def abrir_qr():
    global valorQRLeido
    file = fd.askopenfilename()
    label4 = customtkinter.CTkLabel(master=tabview.tab("Leer"), text='', fg_color="transparent")
    label4.place(relx=0.5, rely=0.37, anchor=tkinter.CENTER)
    label4.configure(image=tkinter.PhotoImage(file=f"{file}"))
    image = Image.open(file)
    img = cv2.imread(f'{file}')
    det = cv2.QRCodeDetector()
    valorQRLeido, pts, st_code = det.detectAndDecode(img)
    # Mostramos el valor del QR leído
    print("El valor del QR leído es: ", valorQRLeido)
    textbox.insert("0.0", f"{valorQRLeido}")
def limpiar():
    textbox.delete("0.0", "end")

def guardar_txt():
    try:
        archivo = open(f"{date.today()}.txt", 'w')
        archivo.write(valorQRLeido)
        archivo.close()
    except:
        print('hubo un error al guardar por favor suicidate')



