def generear_qr():
    global im
    qr.clear()
    data = entry1.get()
    print('qr generado',data)
    qr.add_data(data)
    qr.make(fit=True)
    im = qr.make_image(fill_color="#000000", back_color="white")
    im.save(f'{entry2.get()}.png')
    label3.configure(image=tkinter.PhotoImage(file=f"{entry2.get()}.png"))
